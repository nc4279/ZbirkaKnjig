<h2>Zabavni kviz</h2>
<p>Da na našo spletno stran dodam malo zabave, sem vam ustvarila en zabaven, a poučen kviz, kjer lahko poskusite vaše znanje o knjižebnosti.
    Kviz vsebuje 10 naključnih vprašanj, pri vsakemu vprašanju so podani 3 odgovori, ampak samo en odgovor je pravilen. Na koncu kviza se vam prikaže rezultat,
    kako uspešni ste bili. <br>
    Želim vam veliko uspeha in zabave pri reševanju kviza, <br> 
    <i>Nina</i> 
</p>